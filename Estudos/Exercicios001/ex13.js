function dia(x){
    switch(x){
        case 1:
        console.log('Hoje é domingo, é dia de descanso')
        break
        case 2:
        console.log('Hoje é segunda, é dia útil')
        break
        case 3:
        console.log('Hoje é terça, é dia útil')
        break
        case 4:
        console.log('Hoje é quarta, é dia útil')
        break
        case 5:
        console.log('Hoje é quinta, é dia útil')
        break
        case 6:
        console.log('Hoje é sexta, é dia útil')
        break
        case 7:
        console.log('Hoje é sábado, é dia de descanso')
        break
    }
  
}

dia(5)
